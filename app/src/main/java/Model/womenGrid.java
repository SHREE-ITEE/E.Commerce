package Model;

public class womenGrid {
    String image,price;

    public womenGrid(String image,String price) {
        this.image = image;
        this.price=price;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
}
